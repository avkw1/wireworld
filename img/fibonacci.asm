Программа вычисления чисел Фибоначчи.
Выводит на экран числа 0, 1, 1, 2, 3, 5, 8, ...

 1  3c07  MOV R60,R7   ; R60 := R7
 2  0007  MOV R0 ,R7   ; R7 -> экран
 3  073d  MOV R7 ,R61  ; R7 := R60 + R61
 4  3f06  MOV R63,R6   ; Переход на адрес 1
 5  3d3c  MOV R61,R60  ; R61 := R60 (branch delay slot)
 6  0001  1
 7  0001  1

Содержимое памяти:

0x3c07
0x0007
0x073d
0x3f06
0x3d3c
0x0001
0x0001
